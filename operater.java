class Operater
{
	public static void main(String[] args)
	{
		int a=10,b=2;
		char operater='/';
		double result;
		if(operater=='+')
		{
			result=a+b;
			System.out.println(result);
		}
		if(operater=='-')
		{
			result=a-b;
			System.out.println(result);
		}
		if(operater=='*')
		{
			result=a*b;
			System.out.println(result);
		}
		if(operater=='/')
		{	
			result=a/b;
			System.out.println(result);
		}
	}
}
		